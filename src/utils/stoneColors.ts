export function loadPlayerColor(): string {
  return localStorage.getItem('stone_color') || 'sandstone';
}

export function savePlayerColor(id: string) {
  localStorage.setItem('stone_color', id);
  // Also save to DB for cross-device sync
  syncColorToDb(id);
}

async function syncColorToDb(colorId: string) {
  try {
    const { supabase } = await import('../lib/supabase');
    const token = localStorage.getItem('stone_device_token');
    if (!token) return;
    const { data: player } = await supabase.from('players').select('id').eq('device_token', token).single();
    if (!player) return;

    // Update selected color in player stats
    await supabase.from('player_stats').update({ selected_color: colorId }).eq('player_id', player.id);

    // Update all active games where this player is participating
    await supabase.from('games').update({ p1_color: colorId }).eq('player1_id', player.id).eq('status', 'active');
    await supabase.from('games').update({ p2_color: colorId }).eq('player2_id', player.id).eq('status', 'active');
  } catch { /* silent */ }
}

/** Load selected color from DB and sync to localStorage (call on login) */
export async function syncColorFromDb(playerId: string) {
  try {
    const { supabase } = await import('../lib/supabase');
    const { data } = await supabase.from('player_stats').select('selected_color').eq('player_id', playerId).single();
    if (data?.selected_color) {
      localStorage.setItem('stone_color', data.selected_color);
    }
  } catch { /* silent */ }
}
